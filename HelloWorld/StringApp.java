public class StringApp {
	public static void main(String[] args) {
		
		//Character와 String
		System.out.println("Hello World");//string
		System.out.println('h');//character
		
		
		//new line
		System.out.println("hello \nworld");
		//줄 바꿀때는 \n을 쓴다
		//n은 new line의 약자
		
		//escape
		System.out.println("hello \"world\" ");
		//일반적인 문자로 "를 쓰고 싶을때는
		//"앞에 \를 붙여준다
	}
}